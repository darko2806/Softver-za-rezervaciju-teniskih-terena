    class GradientPanel extends JPanel {

        @Override
        protected void paintComponent(Graphics g) {
            super.paintComponent(g);
            Graphics2D g2d = (Graphics2D) g;
            int width = getWidth();
            int height = getHeight();

            // Boje gradijenta (možeš menjati nijanse po želji)
            Color topColor = new Color(220, 235, 255);   // svetloplava gore
            Color bottomColor = new Color(240, 240, 245); // svetlosiva dole

            GradientPaint gp = new GradientPaint(0, 0, topColor, 0, height, bottomColor);
            g2d.setPaint(gp);
            g2d.fillRect(0, 0, width, height);
        }
    }
