    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
        String naziv = jLabelStatus.getText();
        if (naziv.equals("SERVER JE ISKLJUCEN")) {
            jButton.setText("iskljuci server");
            int port = Integer.parseInt(konfiguracija.Konfiguracija.getInstance().getProperty("port"));
            server = new Server(port);
            server.start();
            jButton.setText("iskljuci server");
            jLabelStatus.setText("SERVER JE UKLJUCEN");
            jLabelStatus.setForeground(Color.green);

        } else {
            jButton.setText("ukljuci server");
            server.ugasiSveKlijente();
            logovani = controller.Controller.getInstance().getLogovaniRecepcionari();
            server.zaustaviServer();
            jLabelStatus.setText(naziv);
            jLabelStatus.setText("SERVER JE ISKLJUCEN");
            jLabelStatus.setForeground(Color.red);

        }
    }//GEN-LAST:event_jButtonActionPerformed

